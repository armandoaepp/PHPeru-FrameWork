class BeanAmigos{
# Constructor
    public function __construct(){}
# Atributos
    private $id;
    private $titulo;
    private $imagen;
    private $imagen_2;
    private $email;
    private $orden;
    private $estado;
    private $fecha;
# METODOS
    public function setId($id_){ $this->id=$id_;}

    public function getId(){ return $this->id;}

    public function setTitulo($titulo_){ $this->titulo=$titulo_;}

    public function getTitulo(){ return $this->titulo;}

    public function setImagen($imagen_){ $this->imagen=$imagen_;}

    public function getImagen(){ return $this->imagen;}

    public function setImagen_2($imagen_2_){ $this->imagen_2=$imagen_2_;}

    public function getImagen_2(){ return $this->imagen_2;}

    public function setEmail($email_){ $this->email=$email_;}

    public function getEmail(){ return $this->email;}

    public function setOrden($orden_){ $this->orden=$orden_;}

    public function getOrden(){ return $this->orden;}

    public function setEstado($estado_){ $this->estado=$estado_;}

    public function getEstado(){ return $this->estado;}

    public function setFecha($fecha_){ $this->fecha=$fecha_;}

    public function getFecha(){ return $this->fecha;}

}
?>