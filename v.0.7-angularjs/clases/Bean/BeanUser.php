<?php 
# Clase Bean Generada  - Creado por @armandoaepp 
class BeanUser{
# Constructor
    public function __construct(){}
# Atributos
    private $user_id;
    private $nombre;
    private $apellidos;
    private $email;
    private $password;
    private $estado;
    private $created_up;
# METODOS
    public function setUser_id($user_id_){ $this->user_id=$user_id_;}
    public function getUser_id(){ return $this->user_id;}
    public function setNombre($nombre_){ $this->nombre=$nombre_;}
    public function getNombre(){ return $this->nombre;}
    public function setApellidos($apellidos_){ $this->apellidos=$apellidos_;}
    public function getApellidos(){ return $this->apellidos;}
    public function setEmail($email_){ $this->email=$email_;}
    public function getEmail(){ return $this->email;}
    public function setPassword($password_){ $this->password=$password_;}
    public function getPassword(){ return $this->password;}
    public function setEstado($estado_){ $this->estado=$estado_;}
    public function getEstado(){ return $this->estado;}
    public function setCreated_up($created_up_){ $this->created_up=$created_up_;}
    public function getCreated_up(){ return $this->created_up;}
}
?>