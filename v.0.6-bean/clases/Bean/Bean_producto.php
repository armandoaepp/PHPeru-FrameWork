class Bean_Producto{
//Constructor
	public function Bean_producto(){}
//Atributos
	private $idproducto;
	private $producto;
	private $idcategoria;
//Propiedades
	public function setIdProducto($idproducto_){ $this->idproducto=$idproducto_;}
	public function getIdProducto(){ return $this->idproducto;}
	public function setProducto($producto_){ $this->producto=$producto_;}
	public function getProducto(){ return $this->producto;}
	public function setIdCategoria($idcategoria_){ $this->idcategoria=$idcategoria_;}
	public function getIdCategoria(){ return $this->idcategoria;}
}
?>