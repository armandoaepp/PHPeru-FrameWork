class BeanAdmision{
# Constructor
    public function __construct(){}
# Atributos
    private $id;
    private $titulo;
    private $imagen;
    private $requisitos;
    private $horarios;
    private $inversion;
    private $email;
# METODOS
    public function setId($id_){ $this->id=$id_;}
    public function getId(){ return $this->id;}
    public function setTitulo($titulo_){ $this->titulo=$titulo_;}
    public function getTitulo(){ return $this->titulo;}
    public function setImagen($imagen_){ $this->imagen=$imagen_;}
    public function getImagen(){ return $this->imagen;}
    public function setRequisitos($requisitos_){ $this->requisitos=$requisitos_;}
    public function getRequisitos(){ return $this->requisitos;}
    public function setHorarios($horarios_){ $this->horarios=$horarios_;}
    public function getHorarios(){ return $this->horarios;}
    public function setInversion($inversion_){ $this->inversion=$inversion_;}
    public function getInversion(){ return $this->inversion;}
    public function setEmail($email_){ $this->email=$email_;}
    public function getEmail(){ return $this->email;}
}
?>